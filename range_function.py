
# range syntax = range(start,stop,step)
# range: The function name that outputs a sequence of numbers
# start: Optional parameter. The number to begin the sequence. This number is included in the sequence. By default, the range function will start at 0
# stop: The number to end the sequence. This number is excluded in the sequence.
# step: Optional parameter. The number to increase each number in the sequence by. By default the range function will step or increase by 1 unless stated otherwise.

for i in range(1,10,1):
  print(i)


for i in range(7,0,-1):
  print(i)